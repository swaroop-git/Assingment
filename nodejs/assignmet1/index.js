function sum(a, b) {
    return a + b;
  }
   
  function multiplication(a, b) {
    return a * b;
  }
   
  function substraction(a, b) {
    return a - b;
  }
   
  function division(a, b) {
    return a / b;
  }
   
  module.exports = { sum, multiplication, substraction, division };